#!/bin/bash
# –°–∫—Ä–∏–ø—Ç –¥–ª—è –ø–∞—Ä—Å–∏–Ω–≥–∞ JSON –ª–æ–≥–æ–≤ –∏–∑ Docker –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä–æ–≤
# –ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ: ./parse-docker-logs.sh <container_name>

CONTAINER_NAME=${1:-"fastapi_app"}

if [ -z "$CONTAINER_NAME" ]; then
    echo "–ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ: $0 <container_name>"
    echo "–ü—Ä–∏–º–µ—Ä: $0 fastapi_app"
    exit 1
fi

# –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä —Å—É—â–µ—Å—Ç–≤—É–µ—Ç
if ! docker ps -a --format '{{.Names}}' | grep -q "^${CONTAINER_NAME}$"; then
    echo "‚ùå –ö–æ–Ω—Ç–µ–π–Ω–µ—Ä ${CONTAINER_NAME} –Ω–µ –Ω–∞–π–¥–µ–Ω"
    exit 1
fi

echo "üìã –õ–æ–≥–∏ –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä–∞ ${CONTAINER_NAME}:"
