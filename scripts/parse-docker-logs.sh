# –ü–æ–ª—É—á–∞–µ–º –ª–æ–≥–∏ –∏ –ø—ã—Ç–∞–µ–º—Å—è —Ä–∞—Å–ø–∞—Ä—Å–∏—Ç—å JSON
docker logs "${CONTAINER_NAME}" 2>&1 | while IFS= read -r line; do
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ —Å—Ç—Ä–æ–∫–∞ –≤–∞–ª–∏–¥–Ω—ã–º JSON
    if echo "$line" | jq . >/dev/null 2>&1; then
        # –ö—Ä–∞—Å–∏–≤–æ —Ñ–æ—Ä–º–∞—Ç–∏—Ä—É–µ–º JSON
        echo "$line" | jq .
    else
        # –ï—Å–ª–∏ –Ω–µ JSON, –≤—ã–≤–æ–¥–∏–º –∫–∞–∫ –µ—Å—Ç—å
        echo "$line"
    fi
done

